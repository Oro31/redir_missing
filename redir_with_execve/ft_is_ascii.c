#include "parsing.h"

int	ft_is_alpha(char c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}

int	ft_is_digit(char c)
{
	return (c >= '0' && c <= '9');
}
